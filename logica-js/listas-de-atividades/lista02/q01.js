// 1. Pergunte ao usuário qual é o dia da semana. Se a resposta for "Sábado" ou "Domingo", mostre 
// "Bom fim de semana!". Caso contrário, mostre "Boa semana!".

let diaDaSemana = prompt ('Que dia é hoje?');

if (diaDaSemana == 'Sábado' || diaDaSemana == 'Domingo') {
    alert ('Bom fim de semana');
} else {
    alert ('Boa semana!');
}